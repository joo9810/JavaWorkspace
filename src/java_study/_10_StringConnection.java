package java_study;
// 문자열과 문자열, 문자열과 정수, 문자열과 실수를 더하게 되면 문자열이 됨
public class _10_StringConnection {
    public static void main(String[] args) {
        System.out.println("5 + 2 = " + 3 + 4);
        System.out.println("5 + 2 = " + 3.1 + 4);
        System.out.println("5 + 2 = " + (3 + 4));
        System.out.println(5 + 2 + " = 3 + 4");
    }
}
